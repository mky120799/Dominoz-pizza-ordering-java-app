    private void btnSendReceiptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSendReceiptMouseClicked
        if(txtUsername.getText().isEmpty()) {
            JOptionPane.showConfirmDialog(null, "Dear please enter username");
        }else if(!isValidEmail(txtEmail.getText())){
            JOptionPane.showConfirmDialog(null, "Dear please enter valid Email");
        }else{
            Ledger ledger = new Ledger(0, txtUsername.getText(), txtEmail.getText(), (totalAmt+(totalAmt*5)/100), null);
            try { 
                LedgerDAO.saveRocerd(ledger);
                sendMail(txtUsername.getText(), txtEmail.getText(), (totalAmt +(totalAmt*5)/100));
                cardLayout.show(cardPanel, "cardMenuHome");
                totalAmt = 0.0;
                cartCount = 0;
                cartData.clear();
                setCartCount();
                
            } catch (SQLException ex) {
                Logger.getLogger(MainScreen.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSendReceiptMouseClicked
    
