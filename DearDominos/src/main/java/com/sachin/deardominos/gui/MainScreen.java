    private void btnOrderHistoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnOrderHistoryMouseClicked
        cardLayout.show(cardPanel, "cardLedger");
        List<Ledger> data = new ArrayList<>();
        try {
            data = LedgerDAO.getAllRecords();
        } catch (SQLException ex) {
            Logger.getLogger(MainScreen.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel model = (DefaultTableModel) tableLedger.getModel();
        for(int i= model.getRowCount() -1; i>=0; i--) {
            model.removeRow(i);
        }
        Object[] row = new Object[6];
        for(Ledger ledger: data) {
            row[0] = ledger.getId();
            row[1] = ledger.getName();
            row[2] = ledger.getEmail();
            row[3] = ledger.getAmt();
            Date d = ledger.getSchedule();
            SimpleDateFormat s1 = new SimpleDateFormat("dd-MM-yyyy");
            row[4] = s1.format(d);
            SimpleDateFormat s2 = new SimpleDateFormat("HH:mm:ss");
            row[5] = s2.format(d);
            model.addRow(row);
        }
    }//GEN-LAST:event_btnOrderHistoryMouseClicked

